UPDATE DAILY_ETL_DATE
SET COB_DATE = NEXT_COB_DATE

DECLARE @START DATE
DECLARE @FLAG INT
SET @START = (SELECT NEXT_COB_DATE FROM DAILY_ETL_DATE)
BEGIN
	WHILE  @START <= DATEADD(DAY, 1, @START)
	BEGIN

		SET @FLAG = (SELECT FLAG
		FROM PVCom..DimCalendar as DL
		WHERE FULL_DATE = DATEADD(DAY, 1, @START)
		)

		SET @START = DATEADD(DAY, 1, @START)

		UPDATE DAILY_ETL_DATE
		SET NEXT_COB_DATE = @START


		IF @FLAG = 1
			BREAK
		
	END
END








